package src1.BasicPractice;

public class AssignmentOp2 {
    public static void main(String[] args) {
        int a=10;
        a+=3;
        System.out.println(a);// 10+3=13
        a-=4;
        System.out.println(a);//13-4=9
        a*=2;
        System.out.println(a);//9*2=18
        a/=2;
        System.out.println(a);//18/2 =9

    }
}
